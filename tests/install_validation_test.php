assertTest("Config contains DB_NAME constant", strpos($generated_config, "define('DB_NAME', 'test_diy_db')") !== false);
assertTest("Config contains DB_USER constant", strpos($generated_config, "define('DB_USER', 'test_user')") !== false);
assertTest("Config handles quote escaping in password", strpos($generated_config, "define('DB_PASS', 'secret\'pass\"word')") !== false);
assertTest("Config contains SITE_URL constant", strpos($generated_config, "define('SITE_URL', 'http://localhost/diy')") !== false);

