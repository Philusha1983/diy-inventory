assertTest("PHP Version 8.2.0 meets 8.0.0 requirement", $mock_php_version_pass === true);
assertTest("PHP Version 7.4.29 fails 8.0.0 requirement", $mock_php_version_fail === false);

$test_extensions = ['pdo', 'pdo_mysql', 'mbstring', 'curl', 'gd'];
$all_exts_loaded = true;
foreach ($test_extensions as $ext) {
    if (!extension_loaded($ext)) {
        $all_exts_loaded = false;
    }
}
echo "  [INFO] Required extensions check: " . ($all_exts_loaded ? "All loaded on current host" : "Some missing on current host") . "\n";
