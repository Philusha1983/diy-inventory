// Simulate database connection testing on invalid credentials
function simulate_db_connection_test($host, $name, $user, $pass) {
    try {
        // Using short timeout to fail fast
        $dsn = "mysql:host=$host;dbname=$name;charset=utf8mb4";
        $conn = @new PDO($dsn, $user, $pass, [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            PDO::ATTR_TIMEOUT => 2
        ]);
        return ['success' => true];
    } catch (PDOException $e) {
        return ['success' => false, 'error' => $e->getMessage()];
    }
}

// We expect this to fail because of invalid host IP
$result_fail = simulate_db_connection_test('192.0.2.1', 'dummy', 'dummy', 'dummy');
assertTest("Connection test with invalid host fails gracefully", $result_fail['success'] === false);
assertTest("Failed connection yields database exception message", !empty($result_fail['error']));

