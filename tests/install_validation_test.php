// We re-implement the masking logic locally to verify it behaves exactly as the installer
function test_mask_data($step, $error_msg, $extra_data) {
    $masked_data = $extra_data;
    if (isset($masked_data['db_pass'])) {
        $masked_data['db_pass'] = '********';
    }
    if (isset($masked_data['admin_password'])) {
        $masked_data['admin_password'] = '********';
    }
    if (isset($masked_data['admin_confirm_password'])) {
        $masked_data['admin_confirm_password'] = '********';
    }
    return $masked_data;
}

$sensitive_post = [
    'db_host' => 'localhost',
    'db_name' => 'diy_db',
    'db_user' => 'admin',
    'db_pass' => 'super_secret_db_password_123',
    'admin_password' => 'secret_admin_pass',
    'admin_confirm_password' => 'secret_admin_pass'
];

$masked_result = test_mask_data('test_step', 'database error', $sensitive_post);

assertTest("Sensitive database password is masked", $masked_result['db_pass'] === '********');
assertTest("Sensitive admin password is masked", $masked_result['admin_password'] === '********');
assertTest("Sensitive admin confirm password is masked", $masked_result['admin_confirm_password'] === '********');
assertTest("Non-sensitive host is preserved", $masked_result['db_host'] === 'localhost');
assertTest("Non-sensitive user is preserved", $masked_result['db_user'] === 'admin');

echo "\n";

