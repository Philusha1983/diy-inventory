function assertTest($description, $assertion) {
    global $pass_count, $fail_count;
    if ($assertion) {
        echo "  [PASS] $description\n";
        $pass_count++;
    } else {
        echo "  [FAIL] $description\n";
        $fail_count++;
    }
}

